
// JSON - JavaScript Object Notation

// JSON is a syntax for storing and exchanging data.
// JSON is text, written with JavaScript object notation.


// 1. Data Representation Format
// 2. Commonly Used for APIs and Configs
// 3. Lightweight and Easy to Read/Write
// 4. Integrated Easily With Most Languages

// _________JSON Types_________

// 1. Strings - "Hello World", "Kyle", "|"
// 2. Numbers - 10, 1.5, -30, 1.2e10
// 3. Booleans - true, false
// 4. Null - null
// 5. Arrays - [1, 2, 3], ["Hello", "World"]
// 6. Objects - { "key": "value" }, { "age": 30 }

// JSON does not have comments
// JSON names require double quotes. JavaScript names don't.


// ________________________example_______________________________________________________

